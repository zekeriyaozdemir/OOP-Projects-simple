#include <iostream>
#include <string>
#include <chrono>
#include <vector>

using namespace std;

class adcDemo2 {
private:
	string channelName;
	int resolution;
	double voltageReference;

public:
	adcDemo2(const string& name, int res, double vRef)
		: channelName(name), resolution(res), voltageReference(vRef) { }
	
	~adcDemo2() { }

	double LSB() const
	{
		return voltageReference / ((1 << resolution) - 1);
	}
	
	double Voltage_Value() const
	{
		return (double) LSB() * 512;
	}

	void print() const
	{
		cout << "------------------------------------------------------------------\n";
		cout << "| " << channelName << "         | "
			<< resolution << " - Bit    | " << (double)voltageReference << "V		 | " << Voltage_Value() << "	 |\n";
	}
};

int main()
{
	auto start = chrono::high_resolution_clock::now();

	int channel = 16;

	vector<adcDemo2> CHs = {
		{"CH_1 ", 16, 3.3},
		{"CH_2 ", 16, 2.5},
		{"CH_3 ", 16, 3.3},
		{"CH_4 ", 16, 4.0},
		{"CH_5 ", 16, 5.0},
		{"CH_6 ", 16, 1.5},
		{"CH_7 ", 16, 1.5},
		{"CH_8 ", 16, 3.3},
		{"CH_9 ", 16, 3.3},
		{"CH_10", 10, 5.0},
		{"CH_11", 10, 5.5},
		{"CH_12", 10, 6.5},
		{"CH_13", 10, 6.0},
		{"CH_14", 10, 5.0},
		{"CH_15", 10, 3.3},
		{"CH_16", 10, 3.3}
	};

	cout << "| Channel Name " << " | Resolution " << "| Reference Voltage " << "| Voltage Value |\n";
	for (int i = 0; i < channel; i++) {
		CHs[i].print();
	}
	cout << "-----------------------------------------------------------------\n\n";

	auto end = chrono::high_resolution_clock::now();
	chrono::duration<double> elapse = end - start;
	cout << "Passing Time: " << elapse.count() << "\n";
	
	return 0;
}

